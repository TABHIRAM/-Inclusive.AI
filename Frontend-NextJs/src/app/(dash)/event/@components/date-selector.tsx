import * as React from "react";
import { addDays, format } from "date-fns";
import { Calendar as CalendarIcon } from "lucide-react";
import { DateRange } from "react-day-picker";

import { cn } from "@/lib/utils";
import { Button } from "@/components/ui/button";
import { Calendar } from "@/components/ui/calendar";
import {
  Popover,
  PopoverContent,
  PopoverTrigger,
} from "@/components/ui/popover";

export function DatePickerWithRange({
  className,
}: React.HTMLAttributes<HTMLDivElement>) {
  const localStorageKey = "dateRange"; // Key for local storage

  // Retrieve date range from local storage on component mount
  const initialDateRange: DateRange | undefined = React.useMemo(() => {
    const storedValue = localStorage.getItem(localStorageKey);
    if (storedValue) {
      return JSON.parse(storedValue);
    }
    return {
      from: new Date(2024, 7, 16),
      to: addDays(new Date(2024, 7, 16), 3),
    };
  }, []);

  // State to manage the date range
  const [date, setDate] = React.useState<DateRange | undefined>(initialDateRange);

  // Update local storage whenever date range changes
  React.useEffect(() => {
    if (date) {
      localStorage.setItem(localStorageKey, JSON.stringify(date));
    }
  }, [date]);

  return (
    <div className={cn("grid gap-2", className)}>
      <Popover>
        <PopoverTrigger asChild>
          <Button
            id="date"
            variant={"outline"}
            className={cn(
              "w-[300px] justify-start text-left font-normal",
              !date && "text-muted-foreground"
            )}
          >
            <CalendarIcon className="mr-2 h-4 w-4" />
            {date?.from ? (
              date.to ? (
                <>
                  {format(date.from, "LLL dd, y")} -{" "}
                  {format(date.to, "LLL dd, y")}
                </>
              ) : (
                format(date.from, "LLL dd, y")
              )
            ) : (
              <span>Pick a date</span>
            )}
          </Button>
        </PopoverTrigger>
        <PopoverContent className="w-auto p-0" align="start">
          <Calendar
            initialFocus
            mode="range"
            defaultMonth={date?.from}
            selected={date}
            onSelect={setDate}
            numberOfMonths={2}
          />
        </PopoverContent>
      </Popover>
    </div>
  );
}
